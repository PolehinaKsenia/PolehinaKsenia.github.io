import re
s = input('Введите предложение: ')
c=0
sarr=re.findall('\w+', s)
print('Количество слов в данной строке:',len(sarr))
for i in range(len(sarr)):
    if int(sarr[i]) % 7 == 0:
        c += 1
print('Количество чисел, кратных 7:',c)

