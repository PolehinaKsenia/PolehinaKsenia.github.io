print(strok(s))

"""
2. Предложение состоит из слов.
    Между словами один или несколько пробелов, после последнего слова «.».
    Определить сколько раз встречается в тексте самое длинное слово.
    (Разбить предложение на массив слов)."""

import re
s = input('Введите предложение: ')
count=0
sarr=re.findall('\w+', s)           #split: ['sdv', 'sdg', '']
maxs = max(sarr, key=len)
for i in range(len(sarr)):
    if sarr[i] == maxs:
        count += 1
print('Самое длинное слово встречается', count, 'раз')

