def strok(a):
    sarr=a.split()
    c1=c2=0
    for i in range(len(sarr)):
        if sarr[i].isdigit():       #ищем именно цифры
            c2 += 1
        else:
            c1 +=1
        if sarr[i] == "0":
            sarr[i] = "1"
    return sarr, c1, c2
s = input('Введите предложение: ')
